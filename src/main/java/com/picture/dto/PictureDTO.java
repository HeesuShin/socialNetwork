package com.picture.dto;

public class PictureDTO {
	private int picture_number;
	private String picture_path;
	private String picture_description;
	private int picture_post_number;
	
	public int getPicture_number() {
		return picture_number;
	}
	public void setPicture_number(int picture_number) {
		this.picture_number = picture_number;
	}
	public String getPicture_path() {
		return picture_path;
	}
	public void setPicture_path(String picture_path) {
		this.picture_path = picture_path;
	}
	public String getPicture_description() {
		return picture_description;
	}
	public void setPicture_description(String picture_description) {
		this.picture_description = picture_description;
	}
	public int getPicture_post_number() {
		return picture_post_number;
	}
	public void setPicture_post_number(int picture_post_number) {
		this.picture_post_number = picture_post_number;
	}
	
	
}
